import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";
import { cn } from "@/lib/utils";

const badgeVariants = cva(
  "inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default: "border-transparent bg-white/10 text-white",
        secondary: "border-transparent bg-white/5 text-neutral-300",
        destructive: "border-transparent bg-red-500/20 text-red-300",
        success: "border-transparent bg-green-500/20 text-green-300",
        outline: "border-white/20 text-white",
        newcomer: "border-transparent bg-red-500/20 text-red-300",
        silver: "border-transparent bg-amber-500/20 text-amber-300",
        gold: "border-transparent bg-green-500/20 text-green-300",
        platinum: "border-transparent bg-emerald-500/20 text-emerald-300",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
);

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return (
    <div className={cn(badgeVariants({ variant }), className)} {...props} />
  );
}

export { Badge, badgeVariants };
